import Foundation
public typealias EmptyClosure = () -> Void

public typealias TypeClosure<T, R> = (T) -> R

public typealias BoolClosure = (Bool) -> Void
public typealias StringClosure = (String) -> Void
public typealias IntClosure = (Int) -> Void
